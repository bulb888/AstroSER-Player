class I18n(QObject):
    """Internationalization singleton."""

    language_changed = Signal(str)
    _instance = None

    def __init__(self):
        super().__init__()
        self._lang = "zh"

    @classmethod
    def instance(cls) -> "I18n":
        if cls._instance is None:
            cls._instance = cls()
            # Load saved language
            settings = QSettings("AstroSER", "Player")
            saved = settings.value("language", "zh")
            if saved in LANGUAGES:
                cls._instance._lang = saved
        return cls._instance

    @property
    def lang(self) -> str:
        return self._lang

    def set_language(self, lang: str) -> None:
        if lang in LANGUAGES and lang != self._lang:
            self._lang = lang
            settings = QSettings("AstroSER", "Player")
            settings.setValue("language", lang)
            self.language_changed.emit(lang)

    def t(self, key: str) -> str:
        """Get translated string for current language."""
        strings = _STRINGS.get(key)
        if strings is None:
            return key
        return strings.get(self._lang, strings.get("en", key))


def tr(key: str) -> str:
    """Shortcut to get translated string."""
    return I18n.instance().t(key)
